int check_encoding(const char *encoding) {
  int size = sizeof(encode_names) / sizeof(encode_names[0]);
  for (int i = 0; i < size; i++) {
      if (strcmp(encode_names[i], encoding) == 0) {
          return i;
      }
  }
  
  return -1;
}